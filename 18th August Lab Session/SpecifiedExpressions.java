package Lab18thAugust;

public class SpecifiedExpressions {

	public static void main(String[] args) {  //creating the main method
		System.out.println("25/3 = " + (25/3));
		System.out.println("(4.0e-5*1000.11) =" + (4.0e-5*1000.11));
		System.out.println("(true && true)=" + (true && true));
		System.out.println("false && true) ="+(false && true));
		System.out.println("(false&&false)||(true&&true) ="+((false&&false)||(true&&true)));
		System.out.println("(false||false)&&(true&&true) ="+((false||false)&&(true&&true)));
		
		

	}

}
