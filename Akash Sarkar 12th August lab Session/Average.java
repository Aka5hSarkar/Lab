//Q.1.Write a Java program that takes five numbers as input to calculate and print the average of the numbers.

class Average
{
public static void main(String[] args)
{
int a=10;
int b=20;
int c=30;
int d=40;
int e=50;
System.out.println("The five numbers are:" + a + "," + b + "," + c + "," + d + "," + e);
int average=(a+b+c+d+e)/5; 
System.out.println("The average is :" + average);
}
}